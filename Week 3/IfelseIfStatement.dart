void main() {
  var age = 18;
  if (age > 18) {
    print("Lydia is a voter in Kenya");
  } else if (age == 18) {
    print("Lydia just became eligible to vote in Kenya");
  } else {
    print("Lydia is still young to vote");
  }
}

/*
If age is greater than 18, the statement "Lydia is a voter in Kenya" will be printed.
If age is exactly 18, the statement "Lydia just became eligible to vote in Kenya" will be printed.
If neither of the above conditions is met, the statement "Lydia is still young to vote" will be printed.
*/ 